let sortAsc = true;

function renderDictionary(entries) {
    const spanishTbody = document.getElementById('spanish-dictionary');
    const frenchTbody = document.getElementById('french-dictionary');
    spanishTbody.innerHTML = '';
    frenchTbody.innerHTML = '';
    
    entries.forEach(entry => {
        // Renderizar en la tabla de español
        const spanishRow = document.createElement('tr');
        const spanishCell = document.createElement('td');
        spanishCell.textContent = entry.spanish;
        spanishCell.addEventListener('click', () => showMeaning(entry, 'spanish'));
        spanishRow.appendChild(spanishCell);
        spanishTbody.appendChild(spanishRow);

        // Renderizar en la tabla de francés
        const frenchRow = document.createElement('tr');
        const frenchCell = document.createElement('td');
        frenchCell.textContent = entry.french;
        frenchCell.addEventListener('click', () => showMeaning(entry, 'french'));
        frenchRow.appendChild(frenchCell);
        frenchTbody.appendChild(frenchRow);
    });
}

function showMeaning(entry, language) {
    const selectedWordDiv = document.getElementById('selected-word');
    selectedWordDiv.style.display = 'block'; // Mostrar el div si está oculto
    selectedWordDiv.innerHTML = ''; // Limpia el contenido anterior

    let meaning;
    if (language === 'spanish') {
        meaning = entry.meaningSpanish;
        selectedWordDiv.innerHTML = `<h3>Español: ${entry.spanish}</h3>`;
    } else if (language === 'french') {
        meaning = entry.meaningFrench;
        selectedWordDiv.innerHTML = `<h3>Francés: ${entry.french}</h3>`;
    }
    selectedWordDiv.innerHTML += ' <button id="close-btn" class="close-btn">X</button>';
    selectedWordDiv.style.display = 'block';

    document.getElementById('close-btn').addEventListener('click', function() {
        selectedWordDiv.style.display = 'none';
    });

    // Crea una lista con los significados
    const ul = document.createElement('ul');
    for (const key in meaning) {
        if (meaning.hasOwnProperty(key)) {
            const li = document.createElement('li');
            li.innerHTML = `${key.charAt(0).toUpperCase() + key.slice(1)}: ${meaning[key]}`;
            ul.appendChild(li);
        }
    }
    selectedWordDiv.appendChild(ul);
}

function sortTable(columnIndex, language) {
    sortAsc = !sortAsc;
    const table = language === 'spanish' ? document.getElementById('spanish-table') : document.getElementById('french-table');
    const rowsArray = Array.from(table.getElementsByTagName('tr')).slice(1);
    rowsArray.sort((a, b) => {
        const aText = a.getElementsByTagName('td')[columnIndex].textContent;
        const bText = b.getElementsByTagName('td')[columnIndex].textContent;
        return sortAsc ? aText.localeCompare(bText) : bText.localeCompare(aText);
    });
    const tbody = table.getElementsByTagName('tbody')[0];
    rowsArray.forEach(row => tbody.appendChild(row));
}

